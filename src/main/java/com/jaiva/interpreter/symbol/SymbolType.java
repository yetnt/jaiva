package com.jaiva.interpreter.symbol;

/**
 * SymbolType enum represents the different types of symbols in the Jaiva
 * language.
 */
public enum SymbolType {
    /**
     * Represents a variable symbol.
     * <p>
     * This type is used for variables that can hold values and can be modified.
     */
    VARIABLE,
    /**
     * Represents a function symbol.
     * <p>
     * This type is used for functions that can be called and executed.
     */
    FUNCTION
}